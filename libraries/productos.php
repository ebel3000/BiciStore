//Funcion para filtrar por marca

function producto_x_marca(string $marca) : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p["marcam"] == $marca){
            //agregar elementos al array
             $resultado[] = $p;
        }
    }

    return $resultado;

}


// Funcion para traer un solo producto para la pagina de producto

function producto_x_id(int $idProducto){
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
         if($p['id'] == $idProducto){
               return $p;
       }
    }

    return null;

}




// Funcion para traer todos los productos

function catalogo_todas_bicis() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        $resultado[] = $p;
    }
        return $resultado;
}


// Funcion para productos destacados

function producto_destacado() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p['destacado'] ?? false){
            $resultado[] = $p;
    }
    }

    return $resultado;

}


